public class Servidor {
    private String nome;
    private String sobrenome;
    private String cpf;
    private boolean ativo;

    // Construtor
    public Servidor(String nome, String sobrenome, String cpf, boolean ativo) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.cpf = cpf;
        this.ativo=ativo;
    }
}